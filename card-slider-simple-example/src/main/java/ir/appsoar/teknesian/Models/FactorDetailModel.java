package ir.appsoar.teknesian.Models;

public class FactorDetailModel {

    private String factorId;
    private String factorStatus;
    private String factorCost;
    private String factorItemsName;
    private String factorItemsCount;
    private String factorItemsCost;

    public String getFactorId() {
        return factorId;
    }

    public void setFactorId(String factorId) {
        this.factorId = factorId;
    }

    public String getFactorStatus() {
        return factorStatus;
    }

    public void setFactorStatus(String factorStatus) {
        this.factorStatus = factorStatus;
    }

    public String getFactorCost() {
        return factorCost;
    }

    public void setFactorCost(String factorCost) {
        this.factorCost = factorCost;
    }

    public String getFactorItemsName() {
        return factorItemsName;
    }

    public void setFactorItemsName(String factorItemsName) {
        this.factorItemsName = factorItemsName;
    }

    public String getFactorItemsCount() {
        return factorItemsCount;
    }

    public void setFactorItemsCount(String factorItemsCount) {
        this.factorItemsCount = factorItemsCount;
    }

    public String getFactorItemsCost() {
        return factorItemsCost;
    }

    public void setFactorItemsCost(String factorItemsCost) {
        this.factorItemsCost = factorItemsCost;
    }

}